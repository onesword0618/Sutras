// 問題2-16
// String型の文字列の初期化と変数に文字列を代入するプログラム
class StringTester {
	public static void main(String[] args) {

	String s1 = "ABC";
	String s2 = "XYZ";

		System.out.println("文字列s1" + s1 + "です");
		System.out.println("文字列s2" + s2 + "です");

		s1 = "FBI";

		System.out.println("文字列s1" + s1 + "です");
		System.out.println("文字列s2" + s2 + "です");
	}
}
