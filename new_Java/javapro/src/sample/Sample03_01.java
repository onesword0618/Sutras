package sample;
// 整数リテラル
public class Sample03_01 {
	
	public static void main(String[] args) {
		//	Long型の値
		System.out.println(127L);
		//	16進数
		System.out.println(0x7f);
		//	8進数
		System.out.println(0177);
		//	2進数
		//	整数リテラルは、何進数の値でも、数字と数字の間にアンダーバーを挿入することができる
		System.out.println(0b0111_1111);
	}
}